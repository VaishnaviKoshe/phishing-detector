def build_security_recommendations(result):

    risk = result["result"]["risk"]

    recommendations = []

    if risk == "SAFE":
        color = GREEN

        icon = "✔"

        recommendations = [
            "Always verify the domain before entering credentials.",
            "Continue using HTTPS websites.",
            "Keep browsers and antivirus updated.",
            "Use VirusTotal for unknown links.",
            "Remain cautious of unexpected emails."
        ]

    elif risk == "SUSPICIOUS":
        color = ORANGE

        icon = "⚠"

        recommendations = [
            "Verify the sender before opening the website.",
            "Avoid entering passwords until verified.",
            "Inspect the SSL certificate carefully.",
            "Cross-check the URL using VirusTotal.",
            "Report suspicious URLs if necessary."
        ]

    else:
        color = RED

        icon = "✖"

        recommendations = [
            "Do NOT open this website.",
            "Never enter passwords or OTPs.",
            "Report this URL immediately.",
            "Block the domain if used inside an organization.",
            "Delete emails containing this URL."
        ]

    rows = []

    for item in recommendations:

        rows.append([
            Paragraph(
                f"<font color='{color}'><b>{icon} {item}</b></font>",
                BODY_STYLE
            )
        ])

    table = Table(rows, colWidths=[6.7*inch])

    table.setStyle(TableStyle([

        ("BACKGROUND",(0,0),(-1,-1),WHITE),

        ("BOX",(0,0),(-1,-1),0.5,colors.HexColor("#D1D5DB")),

        ("RIGHTPADDING",(0,0),(-1,-1),8),

        ("BOTTOMPADDING",(0,0),(-1,-1),6),

        ("TOPPADDING",(0,0),(-1,-1),6),

        ("LEFTPADDING",(0,0),(-1,-1),8),

    ]))

    return [
        Paragraph("<b>6. SECURITY RECOMMENDATIONS</b>", SECTION_STYLE),
        Spacer(1,8),
        table
    ]

