def build_detection_reasons(result):

    findings = []

    # HTTPS
    if result["checks"]["https"]:
        findings.append(("GOOD", "HTTPS protocol is enabled."))
    else:
        findings.append(("BAD", "HTTPS protocol is not enabled."))

    # SSL
    if result["ssl"]["status"] == "Valid":
        findings.append(("GOOD", "SSL certificate is valid."))
    else:
        findings.append(("BAD", "SSL certificate is invalid."))

    # DNS
    if result["checks"]["dns_status"] == "Valid":
        findings.append(("GOOD", "DNS records resolved successfully."))
    else:
        findings.append(("BAD", "DNS resolution failed."))

    # Domain Age
    age = result["checks"]["domain_age"]

    if age >= 35:
        findings.append(("GOOD", f"Domain age: {age} days"))

    elif age >= 90:
        findings.append(("WARNING", f"Domain is relatively new ({age} days)"))

    else:
        findings.append(("BAD", f"Very new domain ({age} days)"))

    # VirusTotal
    if result["virustotal"]["malicious"] == 0 and result["virustotal"]["suspicious"] == 0:
        findings.append(("GOOD", "No malicious detections in VirusTotal."))

    elif result["virustotal"]["malicious"] == 0:
        findings.append(("WARNING", "Suspicious detections found."))

    else:
        findings.append(("BAD", "Malicious detections found."))

    
    rows = []

    for status, message in findings:

        if status == "GOOD":
            color = GREEN
            icon = "✔"

        elif status == "WARNING":
            color = ORANGE
            icon = "⚠"

        else:
            color = RED
            icon = "✖"

        rows.append([
            Paragraph(
                f"<font color='{color}'><b>{icon} {message}</b></font>",
                BODY_STYLE
            )
        ])

    
    table = Table(rows, colWidths=[6.7*inch])

    table.setStyle(TableStyle([
        ("BACKGROUND",(0,0),(-1,-1),WHITE),

        ("TOPPADDING",(0,0),(-1,-1),4),
        
        ("BOTTOMPADDING",(0,0),(-1,-1),4),

        ("BOX",(0,0),(-1,-1),0.5,colors.HexColor("#D1D5DB")),

        ("TOPPADDING",(0,0),(-1,-1),4),

        ("LEFTPADDING",(0,0),(-1,-1),4),

    ]))

    return table

